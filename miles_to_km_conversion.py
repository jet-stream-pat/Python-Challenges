kilometers = 12.25
miles = 7.38

miles_to_km = miles * 1.6
km_to_miles = kilometers / 1.6

print(miles_to_km)
print(km_to_miles)

print(miles, 'miles converted to kilometres is', round(miles_to_km, 2), 'rounded to two decimal places.')

print(kilometers, 'kilometers converted to miles is', round(km_to_miles, 2), 'rounded to two decimal places.')
